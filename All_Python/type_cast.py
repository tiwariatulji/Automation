# type casting
def type_casting():
    """
    This function demonstrates type casting in Python.
    It shows examples of converting variables from one data type to another.
    """
    
    # Convert an integer to a float
    x = 10
    y = float(x)
    print(y)  # Output: 10.0
    
    # Convert a float to an integer
    x = 5.768
    y = int(x)
    print(y)  # Output: 5
    
    # Convert a string to an integer
    x = "10"
    y = int(x)
    print(y)  # Output: 10

    K = "Viraj"
    M = type(K) 
    print(M)
